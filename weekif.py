day = str(input("enter day : "))
if day == "sunday" :
    print(day , "is holiday ^_^")
    
elif day == "saturday":
    print(day , "is half day *_*")

elif day == "monday" or day == "tuesday" or day == "wednesday" or day == "thursday" or day == "friday":
    print(day , "is working day 0_0")
    
else:
    print(day , "is out of cosmos")


